import { Injectable } from '@nestjs/common';
import { calendar_v3 } from 'googleapis';

@Injectable()
export class GoogleCalendarService {
  async getEventByTitle(
    calendar: calendar_v3.Calendar,
    calendarId: string,
    title: string,
  ) {
    const res = await calendar.events.list({
      calendarId,
      q: title,
      singleEvents: true,
      maxResults: 10,
      orderBy: 'startTime',
    });

    const events = res.data.items ?? [];

    const matched = events.find((event) => event.summary === title);

    return matched || null;
  }

  async createEvent(
    calendar: calendar_v3.Calendar,
    calendarId: string,
    eventData: any,
  ) {
    console.log('Creating even: ', eventData);
    return calendar.events.insert({
      calendarId,
      requestBody: {
        summary: eventData.title,
        description: eventData.description || 'Generated by Reacter AI',
        start: {
          dateTime: new Date(eventData.startDateTime).toISOString(),
          timeZone: 'Asia/Kolkata',
        },
        end: {
          dateTime: new Date(eventData.endDateTime).toISOString(),
          timeZone: 'Asia/Kolkata',
        },
      },
    });
  }

  async updateEvent(
    calendar: calendar_v3.Calendar,
    calendarId: string,
    eventId: string,
    eventData: any,
  ) {
    return calendar.events.update({
      calendarId,
      eventId,
      requestBody: {
        summary: eventData.title,
        description: eventData.description || 'Updated by Reacter AI',
        start: {
          dateTime: new Date(eventData.startDateTime).toISOString(),
          timeZone: 'Asia/Kolkata',
        },
        end: {
          dateTime: new Date(eventData.endDateTime).toISOString(),
          timeZone: 'Asia/Kolkata',
        },
      },
    });
  }

  async deleteEvent(
    calendar: calendar_v3.Calendar,
    calendarId: string,
    eventId: string,
  ) {
    return calendar.events.delete({
      calendarId,
      eventId,
    });
  }
}
